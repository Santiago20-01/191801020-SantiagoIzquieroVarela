    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        int id = Integer.parseInt(jTextField4.getText());
        
        HerramientaMaterial herramientaMaterial = new HerramientaMaterial(id);
        
        try {
            DaoHerramientaMaterial.eliminar(herramientaMaterial);
        } catch (SQLException ex) {
            Logger.getLogger(VentanaHerramienteMaterial.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally{
            jTextField4.setText(" ");
            jTextField5.setText(" ");
            jTextField6.setText(" ");
            jTextField7.setText(" ");
            try {
                this.crearModelo();
            } catch (SQLException ex) {
                Logger.getLogger(VentanaCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
            jList1.setModel(modelo);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

